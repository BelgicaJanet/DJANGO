# for imprimira miles de veces
#range es una función que crea una lista de números en serie (estos números son
#proporcionados por ti como parámetros).
#Ten en cuenta que el segundo de estos dos números no será incluido en la lista que
#retornará Python (es decir, range(1, 6) cuenta desde 1 a 5, pero no incluye el número 6).
for i in range(1,10):
	print(i)